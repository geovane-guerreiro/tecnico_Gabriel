let num2 = 2;

function Soma(){
    return num1 + num2;
}

function Subtracao(){
    return num1 - num2;
}

function Divisao(){
    return num1 / num2;
}
function RestoDivisao(){
    return num1 % num2;
}


function Multiplicacao(){
return num1 * num2;
}




